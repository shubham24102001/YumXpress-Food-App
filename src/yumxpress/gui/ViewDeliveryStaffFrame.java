    private void loadAllDetails() {
        //To change body of generated methods, choose Tools | Templates.
        try
        {
            staffList=StaffDAO.getAllStaffIdByCompanyId(OwnerProfile.getCompanyId());
            if(staffList.size()==0){
                JOptionPane.showMessageDialog(null,"No staff in the company ");
                return ;
            }
            for(String staffId:staffList){
                comboStaffId.addItem(staffId);
            }
            
        }
         catch(SQLException sq){
            JOptionPane.showMessageDialog(null,"DB ERROR IN AddDeliveryStaffFrame");
            sq.printStackTrace();
        }
    }

    private void comboStaffIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboStaffIdActionPerformed
        // TODO add your handling code here:
        String staffId=comboStaffId.getSelectedItem().toString();
        try
        {
            StaffPojo staff=StaffDAO.getStaffDetailsbyId(staffId);
            this.txtEmail.setText(staff.getStaffEmailId().toString());
            this.txtName.setText(staff.getStaffName().trim());
        }
        catch(SQLException sq){
            JOptionPane.showMessageDialog(null,"DB ERROR IN AddDeliveryStaffFrame");
            sq.printStackTrace();
        }
    }//GEN-LAST:event_comboStaffIdActionPerformed


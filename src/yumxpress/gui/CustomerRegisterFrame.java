    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        if (validateInput()) {
            return;
        }
        try {
            String custName = txtName.getText().trim();
            String address = txtAddress.getText().trim();
            String email = txtEmail.getText().trim();
            String mobile = txtMobile.getText().trim();
            char[] pass = txtPassword.getPassword();

            String pwd = PasswordEncryption.getEncrytedPassword(String.valueOf(pass));

            CustomerPojo cust = new CustomerPojo();
            cust.setCustomerName(custName);
            cust.setCustomerEmailId(email);
            cust.setCustomerAddress(address);
            cust.setCustomerMobileNo(mobile);
            cust.setCustomerPassword(pwd);

            boolean result = CustomerDAO.addCustomer(cust);
            if (result) {
                JOptionPane.showMessageDialog(null, "Registeration Succssfully", "success", JOptionPane.INFORMATION_MESSAGE);
                Map<String, String> emailCredentials = new HashMap<>();
                emailCredentials.put("emailId", "yumxpress30@gmail.com");
                emailCredentials.put("securityKey", "crnf ftch qmgr gncz");
                Mailer.sendMail(emailCredentials, cust);
                JOptionPane.showMessageDialog(null, "Email Sent Successfully to:" + cust.getCustomerEmailId());
                clearAll();
                return;
            }

            JOptionPane.showMessageDialog(null, "Not Register Please try again!", "Try Again!", JOptionPane.INFORMATION_MESSAGE);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error In Customer register Frame", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        } catch (MessagingException sq) {
            JOptionPane.showMessageDialog(null, "MAIL ERROR IN CustomerRegisterFrame");
            sq.printStackTrace();
        }
    }//GEN-LAST:event_btnRegisterActionPerformed


    private boolean validateInput() {
        //To change body of generated methods, choose Tools | Templates.
        char[] pwd = txtPassword.getPassword();
        if (pwd.length == 0 || txtName.getText().trim().isEmpty() || txtAddress.getText().trim().isEmpty() || txtEmail.getText().trim().isEmpty() || txtMobile.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Fill All the details", "Incomplete data", JOptionPane.INFORMATION_MESSAGE);
            clearAll();
            return true;
        }
        if (Validator.validateEmail(txtEmail.getText().trim()) == false) {
            JOptionPane.showMessageDialog(null, "Please input a Valid Email id", "Invalid Email", JOptionPane.INFORMATION_MESSAGE);
            clearAll();
            return true;
        }

        if (Validator.validateMobileNo(txtMobile.getText().trim())) {
            JOptionPane.showMessageDialog(null, "Please input a Valid Mobile No", "Invalid Mobile No", JOptionPane.INFORMATION_MESSAGE);
            clearAll();
            return true;
        }
        return false;
    }

    private void clearAll() {
        //To change body of generated methods, choose Tools | Templates.
        txtName.setText("");
        txtAddress.setText("");
        txtEmail.setText("");
        txtPassword.setText("");
        txtMobile.setText("");
        txtEmail.requestFocus();

    }
